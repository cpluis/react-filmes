import React from 'react';

// import { Container } from './styles';

function Input({ type="text", label, id, value, setValue, ...props}) {
  return 
  <>
  <label html={id}>{label}</label>
  <input
  id={id}
  type={type}
  value={value}
  onChange={({target}) => setValue(target.value)}
  {...props}
  />
  </>
}

export default Input;